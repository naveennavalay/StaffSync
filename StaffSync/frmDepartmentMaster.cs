        private void btnSaveDetails_Click(object sender, EventArgs e)
        {
            if (string.IsNullOrEmpty(txtDepTitle.Text))
            {
                txtDepTitle.Focus();
                errValidator.SetError(this.txtDepTitle, "Please enter Department Title");
            }
            else if (string.IsNullOrEmpty(txtDepInitial.Text))
            {
                txtDepInitial.Focus();
                errValidator.SetError(this.txtDepInitial, "Please enter Department Initial");
            }
            else if (string.IsNullOrEmpty(cmbIsActive.Text))
            {
                cmbIsActive.Focus();
                cmbIsActive.SelectedIndex = 1;
                errValidator.SetError(this.cmbIsActive, "Please select Department Status");
            }
            else
            {
                if (lblActionMode.Text == "add")
                {
                    int affectedRows = objDepartment.InsertDepartment(txtDepCode.Text.Trim(), txtDepTitle.Text.Trim(), txtDepInitial.Text.Trim(), cmbIsActive.Text.Trim() == "Yes" ? true : false, false);
                    if (affectedRows > 0)
                        MessageBox.Show("Details inserted successfully", "Staffsync", MessageBoxButtons.OK, MessageBoxIcon.Information);
                }
                else if (lblActionMode.Text == "modify")
                {
                    int affectedRows = objDepartment.UpdateDepartment(Convert.ToInt16(lblDepartmentID.Text.Trim()), txtDepCode.Text.Trim(), txtDepTitle.Text.Trim(), txtDepInitial.Text.Trim(), cmbIsActive.Text.Trim() == "Yes" ? true : false, false);
                    if (affectedRows > 0)
                        MessageBox.Show("Details updated successfully", "Staffsync", MessageBoxButtons.OK, MessageBoxIcon.Information);
                }
                onSaveButtonClick();
                disableControls();
                clearControls();
                errValidator.Clear();
            }
        }

